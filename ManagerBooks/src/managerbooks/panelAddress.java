    private void printOut(List<Address> list) {
        String str = "";
        for (Address lts : list) {
            str += "ID: " + lts.getId() + " Latitude: " + lts.getLat() + " Longitude: " + lts.getLog() + " Name : " + lts.getName() + "\n";
        }
        mainView.setText(str);
    }

    private boolean isDouble(String a) {
        String regex_number = "^[+-]?(?:0|[1-9][0-9]*).?[0-9]+$";
        if (Pattern.matches(regex_number, a)) {

            return true;

        }
        return false;
    }
