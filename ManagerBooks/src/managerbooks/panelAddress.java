    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int ck = 0;
        String ids = "";
        Double lats = 0.0;
        Double logs = 0.0;
        if (!listIdAddress.contains(id.getText().trim())) {
            ids = id.getText().trim();
            ck += 1;
            alertId.setText("");

        } else {
            alertId.setText("Id has existted! ");
        }

        if (isDouble(lat.getText().trim())) {
            lats = Double.parseDouble(lat.getText().trim());
            ck += 1;
            alertLat.setText("");
        } else {
            alertLat.setText("Latitude must be double!");
        }
        if (isDouble(log.getText().trim())) {
            logs = Double.parseDouble(log.getText().trim());
            ck += 1;
            alertLog.setText("");
        } else {
            alertLog.setText("Longitude must be double!");
        }
        String names = name.getText().trim();
        if (ck == 3) {
            listIdAddress.add(ids);
            Address newAddress = new Address(ids, lats, logs, names);
            listAdress.add(newAddress);
            alertLog.setText("");
            alertLat.setText("");
            alertId.setText("");
            alertAdd.setText("Add Successfuly!");
            printOut(listAdress);
        } else {
            alertAdd.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

